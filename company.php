&nbsp;&nbsp;&nbsp;The Murphy farm in Ballydesmond was just short of 100 acres, on land that was considered poor for farming, and had more than one peat bog on the land. Like most families in Ireland, the farmhouse was heated by a stove that burned peat, most likely harvested on the Murphy farm. Hannah immigrated to the United States in 1917, married and had four children. My brothers and sisters loved to hear our grandmother, Hannah, tell stories about life on the farm in Ballydesmond. The Murphy home had no indoor plumbing or heat. But Hannah said there was always a warm peat fire to sit by, and the Murphy family was perfectly comfortable. As children, we pressed our grandmother to explain what “peat” was. She said peat was dug out of the bog, that it was earth. When we persisted, asking how can you burn earth, she explained it was “Irish firewood”. That we understood.<br>

